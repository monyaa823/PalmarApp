
package com.example.palmarapp;

    public class QuickLink {
        private String title;
        private int icon;

        public QuickLink(String title, int icon) {
            this.title = title;
            this.icon = icon;
        }

        public String getTitle() {
            return title;
        }

        public int getIcon() {
            return icon;
        }
    }

